-- View: Recent AI Agent Activities
CREATE OR REPLACE VIEW v_recent_ai_activities AS
SELECT 
  'email' as activity_type,
  l.id as lead_id,
  l.name as lead_name,
  et.subject as activity_detail,
  et.sent_at as activity_time,
  CASE WHEN et.opened_at IS NOT NULL THEN 'opened' ELSE 'sent' END as status
FROM email_tracking et
JOIN leads l ON et.lead_id = l.id
UNION ALL
SELECT 
  'zalo' as activity_type,
  l.id as lead_id,
  l.name as lead_name,
  zc.content as activity_detail,
  zc.created_at as activity_time,
  CASE WHEN zc.role = 'assistant' THEN 'replied' ELSE 'received' END as status
FROM zalo_conversations zc
JOIN leads l ON zc.lead_id = l.id
UNION ALL
SELECT 
  'task' as activity_type,
  l.id as lead_id,
  l.name as lead_name,
  at.title as activity_detail,
  at.created_at as activity_time,
  at.status
FROM ai_tasks at
JOIN leads l ON at.lead_id = l.id
ORDER BY activity_time DESC;

-- View: Lead Score Summary
CREATE OR REPLACE VIEW v_lead_score_summary AS
SELECT 
  l.id,
  l.name,
  ls.score,
  ls.classification,
  ls.engagement_score,
  ls.recency_score,
  ls.source_quality_score,
  ls.company_profile_score,
  ls.calculated_at,
  CASE 
    WHEN ls.classification = 'hot' THEN 1
    WHEN ls.classification = 'warm' THEN 2
    ELSE 3
  END as priority_order
FROM leads l
LEFT JOIN lead_scores ls ON l.id = ls.lead_id
WHERE ls.id IN (
  SELECT id FROM lead_scores 
  WHERE lead_id = l.id 
  ORDER BY calculated_at DESC 
  LIMIT 1
);

-- View: AI Agent Performance Metrics
