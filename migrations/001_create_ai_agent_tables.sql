  emails_sent,
  email_open_rate,
  zalo_messages_replied,
  zalo_auto_reply_rate,
  leads_scored,
  leads_hot,
  leads_warm,
  leads_cold,
  tasks_generated,
  task_completion_rate,
  total_gemini_tokens_used,
  total_api_calls,
  average_response_time_ms
FROM ai_agent_usage_stats
ORDER BY date DESC;

